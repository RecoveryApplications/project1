    // ================== Active/Inactive Single ======================
    // ================================================================
    public function activeInactiveSingle($public_values_id, Route $route)
    {
        try {
            $public_values = PublicValue::find($public_values_id);
            if ($public_values) {
                if ($public_values->values == 1) {
                    $public_values->values = 2;  // 2 => Inactive
                    $public_values->save();
