    /**
     * Show the form for editing the specified resource.
     */
    public function edit(string $id, Route $route)
    {
        try {
            $country = Country::find($id);
            return view('admin.countries.edit', compact('country'));
        } catch (\Throwable $th) {
            $function_name =  $route->getActionName();
            $check_old_errors = new SupportTicket();
            $check_old_errors = $check_old_errors->select('*')->where([
                'error_location' => $th->getFile(),
                'error_description' => $th->getMessage(),
                'function_name' => $function_name,
                'error_line' => $th->getLine(),
            ])->get();

            if ($check_old_errors->count() == 0) {
                $new_error_ticket = SupportTicket::create([
                    'error_location' => $th->getFile(),
                    'error_description' => $th->getMessage(),
                    'function_name' => $function_name,
                    'error_line' =>  $th->getLine(),
                ]);
                $end_error_ticket = $new_error_ticket;
            } else {
                $end_error_ticket = $check_old_errors->first();
            }
            return view('errors.support_tickets', compact('th', 'function_name', 'end_error_ticket'));
        }
    }

    /**
     * Update the specified resource in storage.
     */
    public function update(Request $request, string $id, Route $route)
    {
        $request->validate([
            'name_en' => 'required|string|max:255',
            'name_ar' => 'max:255',
        ]);
        if($request->has('key')){
            return redirect()->back()->with('danger', __('You can not change country key'));
        }

        try {
            $country = Country::find($id);
            $country->update($request->except(['_token', '_method']));
            return redirect()->route('super_admin.countries.index')->with('success', 'Country updated successfully');
        } catch (\Throwable $th) {
            $function_name =  $route->getActionName();
            $check_old_errors = new SupportTicket();
            $check_old_errors = $check_old_errors->select('*')->where([
