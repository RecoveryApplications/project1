    public function toggleStatus($id, Route $route)
    {
        try {
            $country = Country::find($id);
            $country->is_active = !$country->is_active;
            $country->save();
            $activeCountriesCount = Country::where('is_active', true)->count();
            if ($activeCountriesCount == 0) {
                $country->is_active = true;
                $country->save();
                return redirect()->back()->with('danger', 'You must have at least one active country');
            }
            return redirect()->back()->with('success', 'Country status updated successfully');
        } catch (\Throwable $th) {
            $function_name =  $route->getActionName();
